export const slugifyTitle = ({ title }: { title: string }): string =>
  slugify(title, {
    strict: true,
    trim: true,
    lower: true,
    replacement: "-",
  });

export const slugifyUsername = ({ name }: { name: string }): string =>
  slugify(name, {
    lower: true,
    trim: true,
    strict: true,
    replacement: "",
    remove: /[^a-zA-Z0-9._]/g,
  });

export const checkProfanity = ({ text }: { text: string }): boolean => {
  if (matcher.hasMatch(text)) {
    return true;
  }

  return hindiGujaratiHinglish.some((word) =>
    text.toLowerCase().includes(word)
  );
};

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

